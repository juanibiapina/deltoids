#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn file_icon_covers_more_languages() {
        // Spot-check a handful of mappings against lazygit v3 glyphs.
        assert_eq!(file_icon("main.kt").glyph, "\u{e634}");
        assert_eq!(file_icon("App.swift").glyph, "\u{f06e5}");
        assert_eq!(file_icon("comp.vue").glyph, "\u{e6a0}");
        assert_eq!(file_icon("comp.svelte").glyph, "\u{e697}");
        assert_eq!(file_icon("data.csv").glyph, "\u{f021b}");
        assert_eq!(file_icon("go.mod").glyph, "\u{e627}");
        assert_eq!(file_icon("flake.nix").glyph, "\u{f313}");
        assert_eq!(file_icon("build.zig").glyph, "\u{e6a9}");
    }

    #[test]
    fn file_icon_known_extensions() {
        assert_eq!(file_icon("lib.rs").glyph, "\u{e68b}");
        assert_eq!(file_icon("doc.md").glyph, "\u{eb1d}");
        assert_eq!(file_icon("Cargo.toml").glyph, "\u{e7a8}"); // exact match wins
        assert_eq!(file_icon("config.json").glyph, "\u{e60b}");
    }

    #[test]
    fn file_icon_carries_lazygit_colors() {
        assert_eq!(file_icon("lib.rs").color, (0xFF, 0x70, 0x43));
        assert_eq!(file_icon("main.go").color, (0x02, 0xAC, 0xC1));
        assert_eq!(file_icon("app.ts").color, (0x01, 0x88, 0xD1));
        assert_eq!(file_icon("index.js").color, (0xFF, 0xCA, 0x29));
        assert_eq!(file_icon("notes.txt").color, (0x42, 0xA5, 0xF5));
    }

    #[test]
    fn file_icon_name_lookup_is_case_sensitive() {
        // lazygit's nameIconMap distinguishes Dockerfile from dockerfile.
        assert_eq!(file_icon("Dockerfile").glyph, "\u{f308}");
        assert_eq!(file_icon("dockerfile").glyph, "\u{f21f}");
    }

    #[test]
    fn file_icon_unknown_extension_falls_back() {
        assert_eq!(file_icon("strange.xyz"), DEFAULT_FILE);
        assert_eq!(file_icon("noextension"), DEFAULT_FILE);
    }

    #[test]
    fn dir_icon_name_lookup_and_fallback() {
        assert_eq!(dir_icon("node_modules").glyph, "\u{e718}");
        assert_eq!(dir_icon("unknown"), ICON_DIR);
        // Deepest segment of a collapsed chain is the lookup key.
        assert_eq!(dir_icon("a/b/bin").glyph, "\u{f12a7}");
    }
}
