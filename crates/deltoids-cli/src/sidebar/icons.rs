//! Icon axis: the nerd-font glyph tables and the [`IconMode`] toggle.
//!
//! The [`NAME_ICONS`] and [`EXT_ICONS`] tables plus the default specs are a
//! verbatim port of lazygit's `pkg/gui/presentation/icons/file_icons.go`
//! (nerd-fonts v3 map values; the `patch...V2` overrides are intentionally
//! ignored). Source commit `93899b82f` (2025-04-10). Each entry carries both
//! lazygit's glyph and its own hex colour, translated to an RGB tuple. Treat
//! the tables as generated data: refresh them by re-copying from lazygit, not
//! by hand-editing individual rows, so they never drift silently.

/// A nerd-font glyph plus its lazygit-defined colour.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub(super) struct IconSpec {
    pub glyph: &'static str,
    pub color: (u8, u8, u8),
}

/// Whether icons were requested at build time (controlled by the
/// `RV_NO_ICONS` environment variable). Captured per-`Sidebar` so tests
/// can override without touching the global env.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum IconMode {
    On,
    Off,
}

impl IconMode {
    /// Read `RV_NO_ICONS` from the environment. Set to anything → off.
    pub fn from_env() -> Self {
        match std::env::var_os("RV_NO_ICONS") {
            Some(v) if !v.is_empty() => IconMode::Off,
            _ => IconMode::On,
        }
    }
}

/// The nerd-font symlink glyph used by the symlink change view.
const SYMLINK_GLYPH: &str = "\u{f481}";

/// Text fallback used for a symlink when icons are disabled (matching
/// `ls -F`'s `@` marker).
const SYMLINK_TEXT: &str = "@";

/// Icon (or text fallback) for the symlink change view, honoring the
/// icon toggle. Returned as a plain `&str` so the deltoids renderers,
/// which own no icon tables, can paint it.
pub fn symlink_icon(mode: IconMode) -> &'static str {
    match mode {
        IconMode::On => SYMLINK_GLYPH,
        IconMode::Off => SYMLINK_TEXT,
    }
}

/// Fallback glyph/colour for files with no name or extension match.
pub(super) const DEFAULT_FILE: IconSpec = IconSpec {
    glyph: "\u{f15b}",
    color: (0x87, 0x87, 0x87),
};

/// Folder glyph used for directory rows. deltoids' tree is always fully
/// expanded, so this is lazygit's default directory icon without the
/// collapse arrow.
pub(super) const ICON_DIR: IconSpec = IconSpec {
    glyph: "\u{f07b}",
    color: (0x87, 0x87, 0x87),
};

/// Icon lookup for the given filename.
///
/// Mirrors lazygit's `IconForFile`: exact basename (case-sensitive) in
/// [`NAME_ICONS`] first, then the lowercased final extension in
/// [`EXT_ICONS`], then [`DEFAULT_FILE`].
pub(super) fn file_icon(name: &str) -> IconSpec {
    let base = name.rsplit('/').next().unwrap_or(name);
    if let Some(spec) = NAME_ICONS.iter().find(|(k, _)| *k == base).map(|(_, v)| *v) {
        return spec;
    }
    // An empty extension never matches: every EXT_ICONS key starts with a dot.
    let ext = file_ext(base).to_ascii_lowercase();
    if let Some(spec) = EXT_ICONS.iter().find(|(k, _)| *k == ext).map(|(_, v)| *v) {
        return spec;
    }
    DEFAULT_FILE
}

/// Icon for a directory row. Mirrors lazygit's directory branch of
/// `IconForFile`: exact-name match in [`NAME_ICONS`] only (no extension
/// map), else the generic [`ICON_DIR`] folder glyph.
pub(super) fn dir_icon(name: &str) -> IconSpec {
    let base = name.rsplit('/').next().unwrap_or(name);
    NAME_ICONS
        .iter()
        .find(|(k, _)| *k == base)
        .map(|(_, v)| *v)
        .unwrap_or(ICON_DIR)
}

/// The final extension of a basename, including the leading dot, matching
/// Go's `filepath.Ext`. Empty string when there is no dot.
fn file_ext(base: &str) -> &str {
    match base.rfind('.') {
        Some(i) => &base[i..],
        None => "",
    }
}

